package vcreature.phenotype;

/**
 * @author Justin Thomas
 * Enumeration, in the order that we will use them, of block vector information
 * names.
 *
 * BlockVector is organized for BlockDNA's size and shape array.
 * BlockVector.enum.ordinal() corresponds to the index of the array where
 * you will find that vector.
 *
 */
public enum BlockVector
{
  CENTER, SIZE, JOINT_A, JOINT_B, AXIS_A, AXIS_B
}
